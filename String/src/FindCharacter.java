//Write a Java program to get the character at the given index within the String.

public class FindCharacter {

	public static void main(String[] args) {
		String givenString = "Java Exercises!";
		System.out.println("Original String = Java Exercises!");                                                                              
		System.out.println("The character at position 0 is "+givenString.charAt(0));
		System.out.println("The character at position 10 is "+givenString.charAt(10));
	}

}
